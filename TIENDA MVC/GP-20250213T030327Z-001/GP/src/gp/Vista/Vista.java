    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Crear un nuevo documento
               String codigo = txtCod.getText();
        String nombre = txtNombre.getText();
        int cantidad = Integer.parseInt(txtInventario.getText());
        double precio = Double.parseDouble(txtPrecio.getText());

        controladorProducto.crearDato(codigo, nombre, cantidad, precio, dtm);
        lblresultado.setText("$" + calcularTotal());  
    
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // Modificar un campo del documento
        int fila = tblTabla.getSelectedRow();
        if (fila != -1) {
            String codigoOriginal = tblTabla.getValueAt(fila, 0).toString();
            String nuevoCodigo = txtCod.getText();
            String nuevoNombre = txtNombre.getText();
            int nuevaCantidad = Integer.parseInt(txtInventario.getText());
            double nuevoPrecio = Double.parseDouble(txtPrecio.getText());

            controladorProducto.modificarDato(codigoOriginal, nuevoCodigo, nuevoNombre, nuevaCantidad, nuevoPrecio, dtm, fila);
            lblresultado.setText("$" + calcularTotal()); 
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Eliminar un documento
                int fila = tblTabla.getSelectedRow();
        if (fila != -1) {
            String codigo = tblTabla.getValueAt(fila, 0).toString();
            controladorProducto.eliminarDato(codigo, dtm, fila);
            lblresultado.setText("$" + calcularTotal()); 
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

