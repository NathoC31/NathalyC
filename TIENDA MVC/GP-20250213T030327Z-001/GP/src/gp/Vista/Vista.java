    // Método para limpiar cajas de texto en JPanel - Vista GUI
    public void limpiarCampos(){
        txtCod.setText("");
        txtNombre.setText("");
        txtInventario.setText("");
        txtPrecio.setText("");
        
        dtm.getDataVector().removeAllElements();
        dtm.fireTableDataChanged();
    }
    public double calcularTotal() {
        double total = 0.0;
        DefaultTableModel model = (DefaultTableModel) tblTabla.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            int cantidad = Integer.parseInt(model.getValueAt(i, 2).toString()); // Columna CANTIDAD
            double precio = Double.parseDouble(model.getValueAt(i, 3).toString()); // Columna PRECIO
            total += cantidad * precio;
        }

        return total;
    }
